                     en el ámbito de la tecnología, la creación de empresas y el diseño de nuevos productos y servicios 
                     en consonancia con las necesidades y expectativas del entorno regional o nacional, señalando así el 
                     camino hacia la pertinencia científica y social de la Facultad.
                    </p>
                    <p>Por creatividad se entiende a la facultad que alguien tiene para crear y a la
                        capacidad creativa de un individuo. Consiste en encontrar procedimientos o
                        elementos para desarrollar labores de manera distinta a la tradicional, con la intención de
                        satisfacer un determinado propósito. La creatividad permite cumplir
                        deseos personales o grupales de forma más veloz, sencilla, eficiente o económica.</p>
